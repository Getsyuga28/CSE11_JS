var grade='A';
switch(grade){
    case 'A':
        console.log("Excellent!");
        break;
    case 'B':
        console.log("Good job!");
        break;
    case 'C':
        console.log("Average.");
        break;
    case 'D':
        console.log("Needs improvement.");
        break;
    case 'F':
        console.log("Failing grade.");
        break;
    default:
        console.log("Invalid grad");
}